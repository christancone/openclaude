def export_restore_cypher(
    session: Any, asset_id: str, output_filename: str = "restore.cypher",
) -> str:
    """Generate ``restore.cypher`` via apoc.export.cypher.query.

    The output is written into the neo4j container's ``/import`` directory
    by default (constrained by NEO4J_apoc_export_file_enabled). The phase
    script then needs to copy it out to the workdir.

    Implementation notes:
      1. APOC's ``apoc.export.cypher.query`` executes the inner query in
         its own context — outer query parameters (``$aid``) are NOT
         propagated. We embed ``asset_id`` directly into the query string.
         Asset IDs are UUIDs (per Q4) so there's no quoting risk; we
         still validate the shape defensively.
      2. APOC writes ``CREATE FULLTEXT INDEX`` and ``CREATE CONSTRAINT``
         statements into the export between nodes and relationships. If
         the destination DB already has those (e.g. from
         ``schema.cypher``), the duplicate-create errors roll back the
         transaction containing the relationship CREATE statements. We
         post-process the file to comment out the schema-creation lines.
         The schema is owned by ``phases/cypher/schema.cypher`` and applied
         separately; restore.cypher is a data-only replay.
    """
    if not all(c.isalnum() or c == "-" for c in asset_id):
        raise ValueError(
            f"export_restore_cypher: asset_id contains unexpected characters: {asset_id!r}"
        )

    inner_query = (
        f'MATCH (n {{asset_id: "{asset_id}"}}) '
        f'OPTIONAL MATCH (n)-[r]->(m {{asset_id: "{asset_id}"}}) '
        f'RETURN n, r, m'
    )
    cypher = """
    CALL apoc.export.cypher.query(
        $inner_query,
        $output_filename,
        {format: "cypher-shell", batchSize: 1000, useOptimizations: {type: "NONE"}}
    )
    YIELD file, nodes, relationships, properties, time
    RETURN file, nodes, relationships, properties, time
    """
    record = session.run(
        cypher, inner_query=inner_query, output_filename=output_filename,
    ).single()
    if not record:
        return ""

    # Post-process: comment out the schema-creation lines so the file is
    # a pure data replay. The schema is owned by phases/cypher/schema.cypher.
    # We do this via APOC itself (filesystem on /import volume).
    sanitize_cypher = """
    CALL apoc.load.json("file:///" + $filename) YIELD value
    WITH collect(value) AS rows
    RETURN size(rows) AS lines
    """
    # Actually APOC can't easily read a .cypher line-by-line, so let
    # apoc.cypher.runFile + sed-style mutation isn't trivial. Simpler:
    # use apoc.import.csv-style file ops. Even simpler: just do the
    # sanitisation client-side from the calling phase script. We do
    # nothing here — phase10.py is responsible for the post-process.
    return record["file"]


def sanitize_restore_cypher_inplace(host_path: str | object) -> int:
    """In-place sanitiser. Comment out ``CREATE FULLTEXT INDEX`` /
    ``CREATE CONSTRAINT`` / ``CREATE INDEX`` lines in a restore.cypher.

    Returns the number of lines commented out.
    """
    return sanitize_restore_cypher(host_path, host_path)


def sanitize_restore_cypher(input_path: str | object, output_path: str | object) -> int:
    """Read a restore.cypher and write a sanitised copy.

    Comments out ``CREATE FULLTEXT INDEX`` / ``CREATE CONSTRAINT`` /
    ``CREATE INDEX`` lines so the resulting file is a pure data replay.
    The schema is owned by ``phases/cypher/schema.cypher`` and applied
    separately. Use this form (rather than in-place) when the input is
    on a read-only mount (e.g. the neo4j-import volume mounted into
    sparengine).

    Returns the number of lines commented out.
    """
    from pathlib import Path
    p_in = Path(input_path)
    p_out = Path(output_path)
    if not p_in.exists():
        return 0
    lines = p_in.read_text(encoding="utf-8").splitlines(keepends=True)
    out: list[str] = []
    n_commented = 0
    for ln in lines:
        s = ln.lstrip()
        if (s.startswith("CREATE FULLTEXT INDEX")
                or s.startswith("CREATE CONSTRAINT")
                or s.startswith("CREATE INDEX ")):
            out.append("// schema (delegated to schema.cypher) — " + ln)
            n_commented += 1
        else:
            out.append(ln)
    p_out.write_text("".join(out), encoding="utf-8")
    return n_commented
