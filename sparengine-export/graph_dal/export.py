"""Phase 10 read queries + APOC export wrappers.

Three deliverables Phase 10 produces:

1. ``graph_export.json`` — the lossless graph projection (an archive of
   every node + edge for this asset). Visualisation is delegated to
   Neo4j Browser; this file is the durable artifact for replays.
2. ``restore.cypher``    — replayable into any Neo4j Community via
   ``cypher-shell -f restore.cypher``. Generated by APOC's
   ``apoc.export.cypher.query`` filtered to the asset's subgraph.
3. ``tier_views.cypher`` — saved-Cypher snippets for Neo4j Browser
   favourites (one per tier; the static ATA→tier rollup mapping that
   replaces :Tier nodes — see Q6).

Functions in this module return Python data structures; phase10.py
serialises them to disk.
"""

from __future__ import annotations

import json
from typing import Any


# =============================================================================
#  graph_export.json projection
# =============================================================================

def export_asset(session: Any, asset_id: str) -> dict:
    """The ``graphData.asset`` block — top-level identification."""
    record = session.run(
        "MATCH (a:Asset {asset_id: $aid}) "
        "OPTIONAL MATCH (a)-[:CERTIFIED_UNDER]->(tc:TypeCertificate) "
        "OPTIONAL MATCH (a)-[:REGISTERED_IN]->(cr:CountryRegistration) "
        "RETURN a {.*} AS asset, tc.value AS tc_value, cr.value AS country",
        aid=asset_id,
    ).single()
    if not record:
        return {}
    asset = dict(record["asset"] or {})
    asset["type_certificate"] = record["tc_value"]
    asset["country_registration"] = record["country"]
    return asset


def export_stats(session: Any, asset_id: str) -> dict:
    """The ``graphData.stats`` block — counts for the panel header."""
    out: dict = {}
    for label in ["Component", "Event", "Finding", "Document", "Page",
                  "Stamp", "Form1", "JobCard", "Person", "Organization",
                  "PriorityItem"]:
        n = session.run(
            "MATCH (n) WHERE n.asset_id = $aid AND $lbl IN labels(n) "
            "RETURN count(*) AS n",
            aid=asset_id, lbl=label,
        ).single()["n"]
        out[f"total_{label.lower()}s" if not label.endswith("y") else f"total_{label.lower()}_items"] = n

    # By severity
    by_sev = list(session.run(
        "MATCH (f:Finding {asset_id: $aid}) "
        "RETURN f.severity AS s, count(*) AS n",
        aid=asset_id,
    ))
    out["findings_by_severity"] = {r["s"] or "unknown": r["n"] for r in by_sev}

    # Components by ATA
    by_ata = list(session.run(
        "MATCH (c:Component {asset_id: $aid}) "
        "RETURN c.ata_chapter AS ata, count(*) AS n ORDER BY n DESC",
        aid=asset_id,
    ))
    out["components_by_ata"] = {(r["ata"] or "unknown"): r["n"] for r in by_ata}

    # Documents by type
    by_dt = list(session.run(
        "MATCH (d:Document {asset_id: $aid}) "
        "RETURN d.document_type AS dt, count(*) AS n ORDER BY n DESC",
        aid=asset_id,
    ))
    out["documents_by_type"] = {(r["dt"] or "unknown"): r["n"] for r in by_dt}

    return out


def export_nodes(session: Any, asset_id: str, limit: int | None = None) -> list[dict]:
    """All visible nodes (asset, components, events, findings, key evidence).

    Returns a list of ``{id, label, group, data}`` dicts in vis-network shape.
    """
    cypher = """
    MATCH (n)
    WHERE n.asset_id = $aid
      AND any(l IN labels(n) WHERE l IN [
        'Asset', 'Component', 'Event', 'Finding', 'Form1', 'CRS',
        'WorkPackage', 'JobCard', 'Stamp', 'Person', 'Organization',
        'PriorityItem', 'TypeCertificate'
      ])
    RETURN n.value AS id, labels(n) AS lbls, n {.*} AS data
    """
    if limit:
        cypher += f" LIMIT {int(limit)}"
    rows = []
    for r in session.run(cypher, aid=asset_id):
        primary = next((l for l in r["lbls"] if l != "Asset" and l != "Component"
                        or r["lbls"][0] == l), r["lbls"][0])
        rows.append({
            "id": r["id"],
            "label": primary,
            "group": primary,
            "data": dict(r["data"] or {}),
        })
    return rows


def export_edges(session: Any, asset_id: str, limit: int | None = None) -> list[dict]:
    """Edges between visible nodes."""
    cypher = """
    MATCH (a)-[r]->(b)
    WHERE a.asset_id = $aid AND b.asset_id = $aid
      AND any(l IN labels(a) WHERE l IN [
        'Asset', 'Component', 'Event', 'Finding', 'Form1', 'CRS',
        'WorkPackage', 'JobCard', 'Stamp', 'Person', 'Organization',
        'PriorityItem', 'TypeCertificate'
      ])
      AND any(l IN labels(b) WHERE l IN [
        'Asset', 'Component', 'Event', 'Finding', 'Form1', 'CRS',
        'WorkPackage', 'JobCard', 'Stamp', 'Person', 'Organization',
        'PriorityItem', 'TypeCertificate'
      ])
    RETURN a.value AS from_id, b.value AS to_id, type(r) AS type, properties(r) AS props
    """
    if limit:
        cypher += f" LIMIT {int(limit)}"
    rows = []
    for r in session.run(cypher, aid=asset_id):
        rows.append({
            "from": r["from_id"],
            "to": r["to_id"],
            "type": r["type"],
            "data": dict(r["props"] or {}),
        })
    return rows


def export_events_by_component(session: Any, asset_id: str) -> dict:
    """``graphData.events[component_id] = [event, ...]`` — time view."""
    cypher = """
    MATCH (e:Event {asset_id: $aid})-[:AFFECTED]->(c:Component {asset_id: $aid})
    RETURN c.value AS comp, e {.*} AS event
    """
    out: dict = {}
    for r in session.run(cypher, aid=asset_id):
        out.setdefault(r["comp"], []).append(dict(r["event"] or {}))
    return out


def export_findings_by_component(session: Any, asset_id: str) -> dict:
    cypher = """
    MATCH (c:Component {asset_id: $aid})-[:HAS_FINDING]->(f:Finding {asset_id: $aid})
    RETURN c.value AS comp, f {.*} AS finding
    """
    out: dict = {}
    for r in session.run(cypher, aid=asset_id):
        out.setdefault(r["comp"], []).append(dict(r["finding"] or {}))
    return out


def export_priority_items(session: Any, asset_id: str) -> list[dict]:
    cypher = """
    MATCH (p:PriorityItem {asset_id: $aid})
    OPTIONAL MATCH (c:Component {asset_id: $aid})-[:HAS_PRIORITY_ITEM]->(p)
    RETURN p {.*} AS item, c.value AS component_uid
    """
    return [
        {**dict(r["item"] or {}), "component_uid": r["component_uid"]}
        for r in session.run(cypher, aid=asset_id)
    ]


def export_mandatory_checklist(session: Any, asset_id: str) -> dict:
    record = session.run(
        "MATCH (a:Asset {asset_id: $aid}) RETURN a.mandatory_checklist AS cl",
        aid=asset_id,
    ).single()
    if record and record["cl"]:
        try:
            return json.loads(record["cl"])
        except Exception:
            return {}
    return {}


def export_lease_return_state(session: Any, asset_id: str) -> dict:
    record = session.run(
        "MATCH (a:Asset {asset_id: $aid}) "
        "RETURN a.lease_return_state AS state, "
        "       a.lease_return_signal_count AS n",
        aid=asset_id,
    ).single()
    if not record:
        return {}
    return {
        "state": record["state"] or "unknown",
        "signal_count": record["n"] or 0,
    }


